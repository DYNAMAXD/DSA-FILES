void countisland(vector<vector<char>>& grid , int row , int col)
{
    if(row <0 || col <0 ||  row == grid.size() || col == grid[0].size())return;
    if(grid[row][col] == '1')
    {
        grid[row][col] = '2';
        //go left
        countisland(grid , row , col-1);
        //go right
        countisland(grid , row , col+1);
        //go up
        countisland(grid , row-1 , col);
        //go down
        countisland(grid , row+1 , col);
    }
    return ;
}

 int numIslands(vector<vector<char>>& grid) {
     int ans = 0;
     for(int i = 0 ; i < grid.size() ; i++)
     {
         for(int j = 0 ; j < grid[0].size() ; j++)
         {
             if(grid[i][j] == '1')
             {
                 ans++;
                 countisland(grid , i , j);
                }
            }
        }
        for(int i = 0 ; i < grid.size() ; i++)
        {
            for(int j = 0 ; j < grid[0].size() ; j++)
            {
                if(grid[i][j] == '2')
                {
                    grid[i][j] = '1';
                }
            }
        }
        return ans;
    }
